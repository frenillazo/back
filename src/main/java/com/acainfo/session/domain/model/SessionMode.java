package com.acainfo.session.domain.model;

/**
 * Mode of delivery for a session.
 */
public enum SessionMode {
    /**
     * In-person session in a physical classroom.
     */
    IN_PERSON,

    /**
     * Online session via virtual classroom.
     */
    ONLINE,

    /**
     * Dual/hybrid session: simultaneous in-person and online attendance.
     */
    DUAL
}
